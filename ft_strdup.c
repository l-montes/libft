#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*str;
	size_t	len;

	len = ft_strlen(s1) + 1;
	str = malloc(len);
	if (!str)
		return (0);
	ft_strlcpy(str, s1, len);
	return (str);
}
